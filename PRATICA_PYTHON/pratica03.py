#TIPOS PRIMITIVOS#

n1 = int(input('Digite um número:')) #int = numeros inteiros = -1, 0, 1, 2, 3, 4, 5, 10, 20, 30,......
print(type(n1))
print(n1)

n2 = float(input('Digite outro número:')) #float = numeros reais = -1.0, 0.5, 1.0, 1.1, 1.2, 2.0, 15.4, ......
print(type(n2))
print(n2)

n3 = bool(input('Digite um valor:')) #bool = se é ou não é um numero = 1 - True , A - False
print(type(n3))
print('E ele é (True / False): {0}'.format(n3))

n4 = str(input('Digite outro valor:')) #str = texto = 'Oi' , '1', 'A', ......
print(type(n4))
print('E ele é (True / False):', n4.isalpha())
print(n4)






