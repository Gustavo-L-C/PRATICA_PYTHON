#FATIAMENTO DE STRIGNS#

f = "HELLO WORLD!"

print(f)

print('1-', f[0:]) #mostramos todo o conteudo da string

print('2-', f[0:5]) #mostramos somente os caracteres de 0 a 5

print('3-', f[6:]) #mostramos somente do caracter 6 em diante

print('4-', f[0:12:2]) #mostrams do caracter 0 ao 12 pulando de 2 em 2 o conteudo da string
